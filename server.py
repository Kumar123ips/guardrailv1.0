class Handler(BaseHTTPRequestHandler):
    def _send(self, code, body, ctype="application/json", extra=None):
        self.send_response(code)
        self.send_header("Content-Type", ctype)
        self.send_header("Content-Length", str(len(body)))
        if extra:
            for k, v in extra.items():
                self.send_header(k, v)
        self.end_headers()
        self.wfile.write(body)

    def _json(self, code, obj):
        self._send(code, json.dumps(obj, ensure_ascii=False).encode("utf-8"))

    def _read_body(self):
        n = int(self.headers.get("Content-Length", 0) or 0)
        if not n:
            return {}
        try:
            return json.loads(self.rfile.read(n).decode("utf-8"))
        except Exception:
            return {}

    def log_message(self, *a):
        pass  # quiet

    def do_GET(self):
        if self.path in ("/", "/index.html"):
            self._send(200, PAGE.encode("utf-8"), "text/html; charset=utf-8")
        elif self.path == "/api/session":
            self._json(200, [r["result"] for r in SESSION])
        elif self.path == "/api/report":
            if not SESSION:
                self._json(404, {"error": "no tests"})
                return
            pdf_bytes = _build_session_pdf()
            self._send(200, pdf_bytes, "application/pdf",
                       {"Content-Disposition": "attachment; filename=guardrail_report.pdf"})
        else:
            self._json(404, {"error": "not found"})

    def do_POST(self):
        if self.path == "/api/check":
            data = self._read_body()
            text = data.get("text", "")
            res = analyze(text)
            d = res.to_dict()
            SESSION.append({"result": d, "raw": text, "verdict": res.verdict,
                            "score": res.score, "families": sorted(res.families)})
            self._json(200, d)
        elif self.path == "/api/suite":
            self._json(200, _run_suite())
        elif self.path == "/api/reset":
            SESSION.clear()
            self._json(200, {"ok": True})
        else:
            self._json(404, {"error": "not found"})


def _run_suite():
    sev = {"ALLOW": 0, "FLAG": 1, "BLOCK": 2}
    passed = 0
    for c in CASES:
        r = analyze(c["text"])
        ok = (r.verdict == "ALLOW") if c["expected"] == "ALLOW" \
            else sev[r.verdict] >= sev[c["expected"]]
        passed += ok
        SESSION.append({
            "result": r.to_dict(), "raw": c["text"], "verdict": r.verdict,
            "score": r.score, "families": sorted(r.families),
            "case": c, "passed": bool(ok),
        })
    total = len(CASES)
    return {
        "total": total, "passed": passed, "failed": total - passed,
        "languages": len({c["language"] for c in CASES}),
        "detection_rate": 100.0 * passed / total if total else 0.0,
    }


def _build_session_pdf():
    results = []
    passed = 0
    by_diff = {}
    for s in SESSION:
        case = s.get("case")
        if case:
            sev = {"ALLOW": 0, "FLAG": 1, "BLOCK": 2}
            ok = (s["verdict"] == "ALLOW") if case["expected"] == "ALLOW" \
                else sev[s["verdict"]] >= sev[case["expected"]]
            difficulty, category, expected = case["difficulty"], case["category"], case["expected"]
        else:
            ok, difficulty, category, expected = True, "interactive", "n/a", "n/a"
        passed += ok
        d = by_diff.setdefault(difficulty, {"total": 0, "passed": 0})
        d["total"] += 1
        d["passed"] += ok
        results.append({
            "text": s["raw"], "category": category, "difficulty": difficulty,
            "expected": expected, "verdict": s["verdict"], "score": s["score"],
            "families": s["families"], "passed": ok,
        })
    total = len(results)
    summary = {
        "total": total, "passed": passed, "failed": total - passed,
        "detection_rate": 100.0 * passed / total if total else 0.0,
        "categories": len({r["category"] for r in results}),
        "languages": "(interactive session)",
        "by_difficulty": by_diff,
    }
    tmp = os.path.join("reports", "_session.pdf")
    os.makedirs("reports", exist_ok=True)
    build_report(tmp, summary, results,
                 title="Guardrail v1.0 — Session Test Report")
    with open(tmp, "rb") as f:
        return f.read()


def main():
    srv = ThreadingHTTPServer((HOST, PORT), Handler)
    print(f"Guardrail UI running at  http://{HOST}:{PORT}")
    print("Press Ctrl+C to stop.")
    try:
        srv.serve_forever()
    except KeyboardInterrupt:
        print("\nStopped.")


if __name__ == "__main__":
    main()
